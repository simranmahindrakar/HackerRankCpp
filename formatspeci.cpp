#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    int n;
    long t;
    char a;
    float f;
    double d;
    scanf("%d %ld %c %f %lf", &n, &t, &a, &f, &d);
    printf("%d \n" , n);
    printf("%ld \n" , t);
    printf("%c \n" , a);
    printf("%f \n" , f);
    printf("%lf \n" , d);

    return 0;
}
